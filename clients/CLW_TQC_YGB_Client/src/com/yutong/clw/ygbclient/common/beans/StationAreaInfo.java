package com.yutong.clw.ygbclient.common.beans;

import org.json.JSONObject;

/**
 * 站点区域信息实体类
 * 
 * @author zhangzhia 2013-10-22 下午4:46:42
 */
public class StationAreaInfo
{
	public String id;

	public String name;

	public void parse(JSONObject optJSONObject)
	{
		id = optJSONObject.optString("id");
		name = optJSONObject.optString("name");

	}

	public String getId()
	{
		return id;
	}

	public void setId(String id)
	{
		this.id = id;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

}
