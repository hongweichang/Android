package com.neusoft.mid.clwapi.entity.statistics;

import java.util.List;

import javax.xml.bind.annotation.XmlElement;

/**
 * @author <a href="mailto:suyingtao@neusoft.com">suyingtao </a>
 * @version $Revision 1.0 $ 2013-3-26 上午10:31:22
 */
public class CarStatReport {
	/**
	 * 有效标志.
	 */
	@XmlElement(name = "valid_flag")
	private String validFlag;
	/**
	 * 车牌号.
	 */
	@XmlElement(name = "car_ln")
	private String carLn;

	/**
	 * 驾驶员.
	 */
	@XmlElement(name = "drive_name")
	private String driveName;
	
	/**
	 * 统计月份，格式：yyyy-mm.
	 */
	@XmlElement(name = "date_time")
	private String dateTime;
	
	/**
	 * 核载人数.
	 */
	@XmlElement(name = "limit_num")
	private String limitNum;
	
	/**
	 * 累计里程.
	 */
	@XmlElement(name = "total_mileage")	
	private String totalMileage;
	
	/**
	 * 累计油耗.
	 */
	@XmlElement(name = "total_oil")
	private String totalOil;
	
	/**
	 * 百公里油耗.
	 */
	@XmlElement(name = "pre_oil")
	private String preOil;
	
	/**
	 * 百公里超速里程.
	 */
	@XmlElement(name = "per_speed_mileage")
	private String perSpeedMileage;
	
	/**
	 * 百公里不良驾驶时长.
	 */
	@XmlElement(name = "per_bad_hour")
	private String perBadHour;
	
	/**
	 * 统计维度信息，包含类型[01][02][03].
	 */
	@XmlElement(name = "detail_info")	
	private List<MoldStatInfo> detailInfo;

	/**
	 * @return Returns the carLn.
	 */
	public String getCarLn() {
		return carLn;
	}

	/**
	 * @param carLn The carLn to set.
	 */
	public void setCarLn(String carLn) {
		this.carLn = carLn;
	}

	/**
	 * @return Returns the dateTime.
	 */
	public String getDateTime() {
		return dateTime;
	}

	/**
	 * @param dateTime The dateTime to set.
	 */
	public void setDateTime(String dateTime) {
		this.dateTime = dateTime;
	}

	/**
	 * @return Returns the limitNum.
	 */
	public String getLimitNum() {
		return limitNum;
	}

	/**
	 * @param limitNum The limitNum to set.
	 */
	public void setLimitNum(String limitNum) {
		this.limitNum = limitNum;
	}

	/**
	 * @return Returns the totalMileage.
	 */
	public String getTotalMileage() {
		return totalMileage;
	}

	/**
	 * @param totalMileage The totalMileage to set.
	 */
	public void setTotalMileage(String totalMileage) {
		this.totalMileage = totalMileage;
	}

	/**
	 * @return Returns the totalOil.
	 */
	public String getTotalOil() {
		return totalOil;
	}

	/**
	 * @param totalOil The totalOil to set.
	 */
	public void setTotalOil(String totalOil) {
		this.totalOil = totalOil;
	}

	/**
	 * @return Returns the detailInfo.
	 */
	public List<MoldStatInfo> getDetailInfo() {
		return detailInfo;
	}

	/**
	 * @param detailInfo The detailInfo to set.
	 */
	public void setDetailInfo(List<MoldStatInfo> detailInfo) {
		this.detailInfo = detailInfo;
	}

	/**
	 * @return Returns the preOil.
	 */
	public String getPreOil() {
		return preOil;
	}

	/**
	 * @param preOil The preOil to set.
	 */
	public void setPreOil(String preOil) {
		this.preOil = preOil;
	}

	/**
	 * @return Returns the perSpeedMileage.
	 */
	public String getPerSpeedMileage() {
		return perSpeedMileage;
	}

	/**
	 * @param perSpeedMileage The perSpeedMileage to set.
	 */
	public void setPerSpeedMileage(String perSpeedMileage) {
		this.perSpeedMileage = perSpeedMileage;
	}

	/**
	 * @return Returns the perBadHour.
	 */
	public String getPerBadHour() {
		return perBadHour;
	}

	/**
	 * @param perBadHour The perBadHour to set.
	 */
	public void setPerBadHour(String perBadHour) {
		this.perBadHour = perBadHour;
	}

	/**
	 * @return Returns the driveName.
	 */
	public String getDriveName() {
		return driveName;
	}

	/**
	 * @param driveName The driveName to set.
	 */
	public void setDriveName(String driveName) {
		this.driveName = driveName;
	}

	/**
	 * @return Returns the validFlag.
	 */
	public String getValidFlag() {
		return validFlag;
	}

	/**
	 * @param validFlag The validFlag to set.
	 */
	public void setValidFlag(String validFlag) {
		this.validFlag = validFlag;
	}

}
