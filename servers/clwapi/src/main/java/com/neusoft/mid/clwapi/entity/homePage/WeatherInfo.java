package com.neusoft.mid.clwapi.entity.homePage;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlElement;

/**
 * @author <a href="mailto:yi_liu@neusoft.com">yi_liu </a>
 * @version $Revision 1.0 $ 2013-4-3 上午9:11:16
 */
public class WeatherInfo implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -7819163177928104790L;
	/**
	 * 日期
	 */
	private String date = "";
	/**
	 * 气温
	 */
	private String temper = "";
	/**
	 * 气象信息
	 */
	private String desc = "";
	/**
	 * 风力
	 */
	private String wind = "";
	/**
	 * 天气图标1，URL
	 */
	@XmlElement(name = "img_one")
	private String imgOne = "";
	/**
	 * 天气图标2，URL
	 */
	@XmlElement(name = "img_two")
	private String imgTwo = "";

	/**
	 * @return Returns the date.
	 */
	public String getDate() {
		return date;
	}

	/**
	 * @param date
	 *            The date to set.
	 */
	public void setDate(String date) {
		this.date = date;
	}

	/**
	 * @return Returns the temper.
	 */
	public String getTemper() {
		return temper;
	}

	/**
	 * @param temper
	 *            The temper to set.
	 */
	public void setTemper(String temper) {
		this.temper = temper;
	}

	/**
	 * @return Returns the desc.
	 */
	public String getDesc() {
		return desc;
	}

	/**
	 * @param desc
	 *            The desc to set.
	 */
	public void setDesc(String desc) {
		this.desc = desc;
	}

	/**
	 * @return Returns the wind.
	 */
	public String getWind() {
		return wind;
	}

	/**
	 * @param wind
	 *            The wind to set.
	 */
	public void setWind(String wind) {
		this.wind = wind;
	}

	/**
	 * @return Returns the dayImg.
	 */
	public String getImgOne() {
		return imgOne;
	}

	/**
	 * @param dayImg
	 *            The dayImg to set.
	 */
	public void setImgOne(String dayImg) {
		this.imgOne = dayImg;
	}

	/**
	 * @return Returns the nightImg.
	 */
	public String getImgTwo() {
		return imgTwo;
	}

	/**
	 * @param nightImg
	 *            The nightImg to set.
	 */
	public void setImgTwo(String nightImg) {
		this.imgTwo = nightImg;
	}

}
