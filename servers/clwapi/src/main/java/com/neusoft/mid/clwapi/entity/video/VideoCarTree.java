
package com.neusoft.mid.clwapi.entity.video;

import javax.xml.bind.annotation.XmlElement;
/**
 * @author wyg
 */
public class VideoCarTree {
	/**
	 * 节点类型。01：组织；02：车辆.
	 */
	@XmlElement(name = "data_type")
	private String dataType;
	
	/**
	 * 组织ID/车辆VIN.
	 */
	@XmlElement(name = "id")
	private String id;
	
	/**
	 * 组织名称/车牌号.
	 */
	@XmlElement(name = "name")
	private String name;
	
	/**
	 * 父组织ID.
	 */
	@XmlElement(name = "pid")
	private String pid;
	
	/**
	 * 当前状态,1-行驶；2-停驶；0-离线.
	 */
	@XmlElement(name = "status")
	private String status;	
	
	/**
	 * 时速，单位km/h.
	 */
	@XmlElement(name = "speed")
	private String speed;
	
	/**
	 * 司机姓名.
	 */
	@XmlElement(name = "driver")
	private String driver;
	
	/**
	 * @return Returns the dataType.
	 */
	public String getDataType() {
		return dataType;
	}

	/**
	 * @param dataType The dataType to set.
	 */
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}

	/**
	 * @return Returns the id.
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id The id to set.
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return Returns the name.
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name The name to set.
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return Returns the pid.
	 */
	public String getPid() {
		return pid;
	}

	/**
	 * @param pid The pid to set.
	 */
	public void setPid(String pid) {
		this.pid = pid;
	}

	/**
	 * @return Returns the carStatus.
	 */
	public String getStatus() {
		return status;
	}

	/**
	 * @param carStatus The carStatus to set.
	 */
	public void setCarStatus(String status) {
		this.status = status;
	}

	/**
	 * @return Returns the speed.
	 */
	public String getSpeed() {
		return speed;
	}

	/**
	 * @param speed The speed to set.
	 */
	public void setSpeed(String speed) {
		this.speed = speed;
	}

	/**
	 * @return Returns the driverName.
	 */
	public String getDriver() {
		return driver;
	}

	/**
	 * @param driverName The driverName to set.
	 */
	public void setDriverName(String driver) {
		this.driver = driver;
	}

}
