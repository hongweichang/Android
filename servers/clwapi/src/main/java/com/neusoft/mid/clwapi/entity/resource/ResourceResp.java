package com.neusoft.mid.clwapi.entity.resource;

/**
 * @author <a href="mailto:yi_liu@neusoft.com">yi_liu </a>
 * @version $Revision 1.0 $ 2013-4-12 上午8:56:18
 */
public class ResourceResp {
	/**
	 * 资源的ID
	 */
	private String resId;
	/**
	 * 资源类型 0- 行业政策 1- 宇通营销 3- 拍摄照片
	 */
	private String type;

	/**
	 * 使用base64方法加密的资源串，如果type 为文字+图片类信息，则其格式为{”img”:”img’s
	 * url”,”text”:”宇通新闻，政策法规”}
	 */
	private String resource;

	/**
	 * @return Returns the type.
	 */
	public String getType() {
		return type;
	}

	/**
	 * @param type
	 *            The type to set.
	 */
	public void setType(String type) {
		this.type = type;
	}

	/**
	 * @return Returns the resource.
	 */
	public String getResource() {
		return resource;
	}

	/**
	 * @param resource
	 *            The resource to set.
	 */
	public void setResource(String resource) {
		this.resource = resource;
	}

	/**
	 * @return Returns the resId.
	 */
	public String getResId() {
		return resId;
	}

	/**
	 * @param resId
	 *            The resId to set.
	 */
	public void setResId(String resId) {
		this.resId = resId;
	}

}
