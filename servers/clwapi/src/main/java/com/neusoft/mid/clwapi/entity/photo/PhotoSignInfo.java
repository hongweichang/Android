package com.neusoft.mid.clwapi.entity.photo;

/**
 * @author <a href="mailto:yi_liu@neusoft.com">yi_liu </a>
 * @version $Revision 1.0 $ 2013-4-12 下午3:50:57
 */
public class PhotoSignInfo {

	/**
	 * 信息的更新时间
	 */
	private String time;
	/**
	 * 标注信息
	 */
	private String info;
	/**
	 * 超载标志
	 */
	private String flag;

	/**
	 * @return Returns the flag.
	 */
	public String getFlag() {
		return flag;
	}

	/**
	 * @param flag
	 *            The flag to set.
	 */
	public void setFlag(String flag) {
		this.flag = flag;
	}

	/**
	 * @return Returns the time.
	 */
	public String getTime() {
		return time;
	}

	/**
	 * @param time
	 *            The time to set.
	 */
	public void setTime(String time) {
		this.time = time;
	}

	/**
	 * @return Returns the info.
	 */
	public String getInfo() {
		return info;
	}

	/**
	 * @param info
	 *            The info to set.
	 */
	public void setInfo(String info) {
		this.info = info;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "PhotoSignInfo [time=" + time + ", info=" + info + ", flag="
				+ flag + "]";
	}

}
