
package com.yutong.axxc.parents.common;


/**
 * 经纬度实体类
 * @author zhangzhia 2013-8-21 下午3:13:39
 *
 */
public class NeuGeoPoint {
    /** 经度 */
    private double x;

    /** 纬度 */
    private double y;

    public NeuGeoPoint() {
    }

    public NeuGeoPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    /**
     * @return Returns the x (longitude).
     */
    public double getX() {
        return x;
    }

    /**
     * @param x The x to set.
     */
    public void setX(double x) {
        this.x = x;
    }

    /**
     * @return Returns the y (latitude).
     */
    public double getY() {
        return y;
    }

    /**
     * @param y The y to set.
     */
    public void setY(double y) {
        this.y = y;
    }

}
