
package com.yutong.axxc.parents.common.beans;

/**
 * 学生乘车信息实体类
 * @author zhangzhia 2013-8-22 上午9:52:20
 *
 */
public class RidingRecordBean {

    /** 学生id  */
    private String cld_id;
    /**  车辆vin */
    private String vehicle_vin;
    /**  车辆号码 */
    private String vehicle_plate;
    /**  车辆速度 */
    private String vehicle_speed;
    /** 1:监控版，2:短信版，3:全能版  */
    private String enterprise_mode;
    /**  驾驶员姓名 */
    private String driver;
    /** 老师姓名  */
    private String teacher;
    /** 老师电话  */
    private String teacher_phone;
    
    public String getCld_id()
    {
        return cld_id;
    }
    public void setCld_id(String cld_id)
    {
        this.cld_id = cld_id;
    }
 
    public String getVehicle_vin()
    {
        return vehicle_vin;
    }
    public void setVehicle_vin(String vehicle_vin)
    {
        this.vehicle_vin = vehicle_vin;
    }
    public String getVehicle_speed()
    {
        return vehicle_speed;
    }
    public void setVehicle_speed(String vehicle_speed)
    {
        this.vehicle_speed = vehicle_speed;
    }
    
    public String getEnterprise_mode()
    {
        return enterprise_mode;
    }
    public void setEnterprise_mode(String enterprise_mode)
    {
        this.enterprise_mode = enterprise_mode;
    }
    public String getDriver()
    {
        return driver;
    }
    public void setDriver(String driver)
    {
        this.driver = driver;
    }
    public String getTeacher()
    {
        return teacher;
    }
    public void setTeacher(String teacher)
    {
        this.teacher = teacher;
    }
    public String getTeacher_phone()
    {
        return teacher_phone;
    }
    public void setTeacher_phone(String teacher_phone)
    {
        this.teacher_phone = teacher_phone;
    }
    public String getVehicle_plate()
    {
        return vehicle_plate;
    }
    public void setVehicle_plate(String vehicle_plate)
    {
        this.vehicle_plate = vehicle_plate;
    }
    
    
    
}
