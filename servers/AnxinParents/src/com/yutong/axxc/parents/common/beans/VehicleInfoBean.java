/**
 * 车辆信息实体类
 * @author zhangzhia 2013-8-22 上午9:52:20
 *
 */
public class VehicleInfoBean {
 
    /** 车辆id  */
    private String vehicle_id;
    /** 车辆vin  */
    private String vehicle_vin;
    /** 车辆代码  */
    private String vehicle_code;
    /**  车牌号码 */
    private String vehicle_plate;
    /** 驾驶员姓名  */
    private String vehicle_driver;
    /** 车辆颜色  */
    private String  vehicle_color;

    public String getVehicle_id()
    {
        return vehicle_id;
    }
    public void setVehicle_id(String vehicle_id)
    {
        this.vehicle_id = vehicle_id;
    }
    public String getVehicle_vin()
    {
        return vehicle_vin;
    }
    public void setVehicle_vin(String vehicle_vin)
    {
        this.vehicle_vin = vehicle_vin;
    }
    public String getVehicle_code()
    {
        return vehicle_code;
    }
    public void setVehicle_code(String vehicle_code)
    {
        this.vehicle_code = vehicle_code;
    }
    public String getVehicle_plate()
    {
        return vehicle_plate;
    }
    public void setVehicle_plate(String vehicle_plate)
    {
        this.vehicle_plate = vehicle_plate;
    }
    public String getVehicle_driver()
    {
        return vehicle_driver;
    }
    public void setVehicle_driver(String vehicle_driver)
    {
        this.vehicle_driver = vehicle_driver;
    }
    public String getVehicle_color()
    {
        return vehicle_color;
    }
    public void setVehicle_color(String vehicle_color)
    {
        this.vehicle_color = vehicle_color;
    }


    
}
